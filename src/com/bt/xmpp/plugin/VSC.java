package com.bt.xmpp.plugin;

public class VSC {
	
	boolean isFree;
	String consoleID;
	String handset;
	String allocatedTraderID;
	String traderName;
	String startTime;
	String endTime;
	
	
	public boolean isFree() {
		return isFree;
	}
	public void setFree(boolean isFree) {
		this.isFree = isFree;
	}
	public String getConsoleID() {
		return consoleID;
	}
	public void setConsoleID(String consoleID) {
		this.consoleID = consoleID;
	}
	public String getHandset() {
		return handset;
	}
	public void setHandset(String handset) {
		this.handset = handset;
	}
	public String getAllocatedTraderID() {
		return allocatedTraderID;
	}
	public void setAllocatedTraderID(String allocatedTraderID) {
		this.allocatedTraderID = allocatedTraderID;
	}
	public String getTraderName() {
		return traderName;
	}
	public void setTraderName(String traderName) {
		this.traderName = traderName;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	
	
	

}
